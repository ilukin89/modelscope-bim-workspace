function createMockFinding(input: MockFindingInput): ReviewIssue {
  return {
    id: input.id,
    code: input.code,
    title: input.title,
    object: input.object,
    location: input.location,
    severity: input.severity,
    status: input.status ?? "Open",
    findingType: input.findingType,
    initialAiStatus: input.initialAiStatus,
    discipline: input.discipline,
    highlight: input.highlight,
    details: {
      shortCode: input.shortCode,
      objectId: input.objectId,
      category: input.category,
      system: input.system,
      type: input.type,
      level: input.level,
      elevation: input.elevation,
      material: input.material ?? "Coordinated assembly",
      fireRating: input.fireRating ?? "Not rated",
      guid: `${input.objectId.replace(/[^A-Za-z0-9]/g, "").slice(0, 5)}…${input.code.slice(-3)}`,
      geometry: defaultGeometryByHighlight[input.highlight],
    },
  }
}

